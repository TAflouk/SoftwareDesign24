def tpl(x):
    """ output: tpl returns thrice its input
    input x: a number (int or float)
    >>>tpl(5)
    15
    >>>tpl(3)
    9
    (num) -> num
    
    """
    return 3*x
#=====================================================================================================================
def sq(x):
    """ output: sq of x
    input x: a number(int or float)
    >>>sq(2)
    4
    (num) -> num
    
    """
    return x**2
